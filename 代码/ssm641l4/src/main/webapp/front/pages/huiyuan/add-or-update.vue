			},
																																																												
			// 多级联动参数
																																																												
																																																									
																																																									
																																																									
																																																															touxiangTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.touxiang = _this.$base.url + 'upload/' + res.file;
